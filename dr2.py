# Задание 9:
# Попросить пользователя ввести текст.В результате
# вывести процент букв в верхнем регистре(заглавные)
# и в нижнем регистре(прописные)
txt = input('Введите текст: ')

def capital():
    low = 0
    up = 0
    i = 0
    while i < len(txt):
        if txt[i].isupper(): up += 1
        if txt[i].islower(): low += 1
        i += 1
    u = up * 100 / (low + up)
    l = low * 100 / (low + up)
    print(u,'%', l, '%')
capital()
